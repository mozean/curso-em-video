#Faça um programa que leia um número inteiro e mostre o seu sucessor e seu antecessor.

print('SUCESSOR E ANTECESSOR DE UM NÚMERO')
numero = int(input('Insira um número inteiro:'))
sucessor = numero + 1
antecessor = numero - 1
print(f'O antecessor de {numero} é {antecessor} e o seu sucessor é {sucessor}')